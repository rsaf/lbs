module.exports = function (paramMongoose) {
  var notificationSchema = paramMongoose.Schema({
    ct: { //creator
      uID: String,//id of the user who created the service
      oID: String,//organisation id
      cd: {type: Date, default: Date.now}//creation date
    },
    md: {//modified
      uID: String,//id of user who changed the service
      oID:String,//organisation id
      lu: {type: Date}//last updated date
    },
    recipient: {
      inmail: {to: String,  ss: {type:Boolean, default: true}},
      weixin:{to:String, ss: {type:Boolean, default: false}},
      sms:{to:String, ss: {type:Boolean, default: false}},
      email:{to:String, ss: {type:Boolean, default: false}}
    },
    notification: {
      viewStatus: {type: Boolean, default: false},
      sendTime: {type: Date, default: Date.now},
      viewTime: Date,
      from: String,
      subject: String,
      body: String,
      notificationType: String,
      messageType: {type:String, default: 'inbox'}
    }
    ,ds:{type:Boolean,default:false}//delete status
  });
  return notificationSchema;
};

