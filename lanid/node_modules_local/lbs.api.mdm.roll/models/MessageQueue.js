module.exports = function(paramMongoose) {
    var messageQueueSchema = paramMongoose.Schema({
        recipients: [{
            inmail:{to:String, ss: {type:Boolean, default: false}},
            weixin:{to:String, ss: {type:Boolean, default: false}},
            sms:{to:String, ss: {type:Boolean, default: false}},
            email:{to:String, ss: {type:Boolean, default: false}}
        }],
        notification:{
            from: String,
            subject:String,
            body:String,
            notificationType:String}
    });
    return messageQueueSchema;
};
