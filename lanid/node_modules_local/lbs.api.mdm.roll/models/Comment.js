module.exports = function (paramMongoose) {
    var commentSchema = paramMongoose.Schema({
        //_id auto created by mongodb
        ct: { //creator
            uID: String,//id of the user who created the service
            oID: String,//organisation id
            cd: {type: Date, default: Date.now}//creation date
        },
        md: {//modified
            uID: String,//id of user who changed the service
            oID:String,//organisation id
            lu: {type: Date}//last updated date
        },
        tguid: String,       //comment target guid // activity code, or any other stuffs in the future
        tct:String,       // target comment topic
        ln: String,     //login name of the person doing the current comment
        av: String,     // to his avater
        cc: String,     //comment content
        cd: {type: Date, default: Date.now}, //creation date
        pc: {pc: {type: Boolean, default: false},cguid: String, ln: String},  //pc parent comment, cguid : parent comment ID, ln: parent comment user login name
        vc: {type:Number,default:0}, // vote counts
        ds:{type:Boolean,default:false}//delete status
    });

    return commentSchema;
};
