var lib = require("lib");
module.exports = function (paramMongoose){

    var requestTypeSchema = new paramMongoose.Schema({
        s : lib.permissionsSchema({all:"r"}),
       _id: String,
       rtn: String, // request type name
       col: String, // collection name the request will affect
       mod: String  // module responsible to commit or roll back when request fails/ is updated
    });

    return requestTypeSchema;


};