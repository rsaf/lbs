#!/bin/sh

# figure out what the full path should be to the image
getPathFromUuid $IMAGE_ROOT $FILE_UUID

ORIGINAL_PHOTO_FILE=${UUID_PATH}/${FILE_UUID}.${FILE_EXT}

# use standard input for the XLSX information
FILESIZE=`stat -f %z $ORIGINAL_PHOTO_FILE`
echo "DATA PHOTO START $FILESIZE"
cat $ORIGINAL_PHOTO_FILE
echo "DATA PHTO END"

exit 0