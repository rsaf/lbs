#
# script to copy photos accross buckets
#

getPathFromUuid $IMAGE_ROOT $FILE_UUID

export photo_to_copy=$UUID_PATH/$FILE_UUID.$FILE_EXT
export destination_bucket=$( echo "$OPTIONS" | cut -d " " -f 1 )
export destination_uuid=$( echo "$OPTIONS" | cut -d " " -f 2 )
export destination_ext=$FILE_EXT

echo "Photo to " $photo_to_copy
echo "Photo destination bucket " $destination_bucket
echo "Photo destination uuid " $destination_uuid
echo "Photo destination extension " $destination_ext

cat $photo_to_copy | ssh -i ~/.ssh/script $destination_bucket "uploadphoto $destination_uuid $destination_ext" || exit 40

