module.exports = function (paramMongoose){

    var photoSchema = new paramMongoose.Schema({
        ct: { //creator
            uID: String,//id of the user who created the service
            oID: String,//organisation id
            cd: {type: Date, default: Date.now}//creation date
        },
        md: {//modified
            uID: String,//id of user who changed the service
            oID: String,//organisation id
            lu: Date//last updated date
        },
        opp:{ //other photos properties
         ign:String, // 照片名称: image name
         igt:String, // 主题类型: 旅游照片 image type
         igs:String, // 拍摄方式: 单板相机 image source
         isl:String, // 拍摄地点:  image shooting location
         rm:String,  // 照片描述: // 30 remarks 备注
         isd:Date,   // 拍摄日期: // image shooting date
         irs:String, // 像素尺寸:84mmX105mm image resolution size //19 output nominal size width 输出名义尺寸宽mm     ////////  //20 output nominal size height 输出名义尺寸高mm    ////////
         ofs:String, // 文件大小:86Kb //28 original photo size 初始照片文件大小            ////////
         ifm:String  // 27 initial format 初始照片格式                                   ////////
        },
        uri: String, // String to physical photo location // AC1279908_SCM15900655434_UC12996987669_OC_2079877898.jpg
        urls: String,
        urll: String,
        urlm: String,
        ds: {type: Boolean, default: false} //true or false
    });
    return photoSchema;
};

