module.exports = function (paramMongoose){

    var fileSchema = new paramMongoose.Schema({
        ct: { //creator
            uID: String,//id of the user who created the service
            oID: String,//organisation id
            cd: {type: Date, default: Date.now}//creation date
        },
        md: {//modified
            uID: String,//id of user who changed the service
            oID: String,//organisation id
            lu: Date//last updated date
        },

            fn:String,  //  document name or file name
            ft:String, // document type or file name
            rm:String,  //30 remarks 备注
            fs:String, // document size  or file size
            ds:{type: Boolean, default:false },
            fm:String,  // document format  or , file format
            uri:String // String to physical photo location // AC1279908_SCM15900655434_UC12996987669_OC_2079877898.jpg
    });
    return fileSchema;
};