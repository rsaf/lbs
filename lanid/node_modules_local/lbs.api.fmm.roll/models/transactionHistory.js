module.exports = function (sequelize, DataTypes) {
    return sequelize.define('transactionHistory', {
        transactionId: {type:DataTypes.STRING, notNull: true, defaultValue:''},
        serviceName: {type:DataTypes.STRING, defaultValue:''},
        transactionAmount: {type: DataTypes.DECIMAL, isDecimal: true, notNull: true, defaultValue:0.0},
        paymentOrderId:{type:DataTypes.INTEGER,isInt: true, notNull: true},
        contextId:{type:DataTypes.INTEGER, notNull: true,  defaultValue:1},
        instanceId:{type:DataTypes.INTEGER, notNull: true,  defaultValue:1},
        sourceAccountId: DataTypes.STRING,
        destinationAccountId: DataTypes.STRING,
        accountBalance:{type: DataTypes.DECIMAL, isDecimal: true}
    },
    {
        tableName: 'transactionHistory',
        freezeTableName: true
    });
   
};

