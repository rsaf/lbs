module.exports  = function (sequelize, DataTypes) {
    return sequelize.define('accountBalance', {
        accountId:{type: DataTypes.STRING, allowNull:false, unique: true, defaultValue:"F0000"},
        accountBalance: {type: DataTypes.DECIMAL, isDecimal: true, defaultValue: 0.00},
        accountType: {type: DataTypes.ENUM, values: ['admin', 'corporate', 'provider','personal'], defaultValue:'personal'},
        totalCashIn:  {type: DataTypes.DECIMAL, isDecimal: true, defaultValue: 0.00},
        totalCashOut:  {type: DataTypes.DECIMAL, isDecimal: true, defaultValue: 0.00}

        }, {
        tableName: 'accountBalance',
        freezeTableName: true
    }
    );
};
