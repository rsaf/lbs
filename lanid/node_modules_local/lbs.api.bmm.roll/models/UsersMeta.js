var lib = require('lib');
module.exports = function (paramMongoose) {
    var UsersMetaSchema = new paramMongoose.Schema({
        s : lib.permissionsSchema({all:""}),
        ct: { //creator
            uID: String, //id of the user who created the service
            oID: String, //organisation id
            cd: {type: Date, default: Date.now} //creation date
        },
        md: {//modified
            uID: String,//id of user who changed the service
            oID: String,//organisation id
            lu: {type: Date, required: true}//last updated date
        },
        ud: { //user list definition
            ln: String, // the name of the user respondents list
            nr: Number, // Number of respondents for respondents list
            cd: Date,  // the date the activity was created
            fs:[String] // fields on the response lists or columns of the respondents list
        },
        siv:[{ // search index values, defines the values that need to be indexed
            in:String,  // name of the search index, example mobile number
            it: String  // type of the search index, String
        }],
        fp:{ //file properties
            fn:String,  //  document name or file name
            ft:String, // document type or file name //
            rm:String,  //30 remarks 备注
            fs:String, // document size  or file size
            fm:String,  // document format  or , file format
            uri:String // String to physical photo location // AC1279908_SCM15900655434_UC12996987669_OC_2079877898.jpg
        },
        ac: String, // activity code
        ds:{type: Boolean, default:false }
    });
    return UsersMetaSchema;
}


