module.exports = function (paramMongoose){
  
var serviceNameSchema = paramMongoose.Schema({
  text: String,
});

//Model Declaration ===============================

//public Methods ===============================
serviceNameSchema.statics.getServiceNamesOrdered = function(paramCallback){
    return this.find()
      .sort('text')
      .exec(paramCallback); //static method

   //db.services.ensureIndex({'ct.cuID':1,'md.lu':-1})
};

return serviceNameSchema;

};